#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*ret;

	ret = b;
	i = 0;
	while (i < len)
	{
		ret[i] = c;
		i++;
	}
	return (b);
}
